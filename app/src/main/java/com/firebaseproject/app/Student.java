package com.firebaseproject.app;


public class Student {
    private String studentId,name,surname,fatherName,nationalId,dob,gender;
    private int id;


    public Student(String studentId, String name, String surname, String fatherName, String nationalId, String dob, String gender, int id) {
        this.studentId = studentId;
        this.name = name;
        this.surname = surname;
        this.fatherName = fatherName;
        this.nationalId = nationalId;
        this.dob = dob;
        this.gender = gender;
        this.id = id;
    }

    public String getStudentId() {
        return studentId;
    }

    public void setStudentId(String studentId) {
        this.studentId = studentId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSurname() {
        return surname;
    }

    public void setSurname(String surname) {
        this.surname = surname;
    }

    public String getFatherName() {
        return fatherName;
    }

    public void setFatherName(String fatherName) {
        this.fatherName = fatherName;
    }

    public String getNationalId() {
        return nationalId;
    }

    public void setNationalId(String nationalId) {
        this.nationalId = nationalId;
    }

    public String getDob() {
        return dob;
    }

    public void setDob(String dob) {
        this.dob = dob;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
